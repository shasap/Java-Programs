public class PositiveNegativeZero {
    static int a = -2;


    public static void main(String[] args) {
        if ( a>0 ){
            System.out.println (a + " = positive number.");
        }else if(a<0){
            System.out.println(a+ " = negative number.");
        }else{
            System.out.println(a +" = zero.");
        }
    }
}
