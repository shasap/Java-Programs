public class CityName {
    static char alphabet = 'w';

    public static void main(String args[])
    {
        if (alphabet == 'a')
        {
            System.out.println("City name is Ahmedabad." );
        }
        else if(alphabet == 'b')
        {
            System.out.println("City name is Baroda");
        }
        else if (alphabet =='c')
        {
            System.out.println("City name is Calcutta.");
        }
        else if(alphabet =='d')
        {
            System.out.println("City name is Dubai.");
        }
        else if(alphabet =='e')
        {
            System.out.println("City name is Ellana.");
        }
        else if(alphabet =='f')
        {
            System.out.println("City name is Florida.");
        }
        else
            {
                System.out.println("invalid.");
            }

    }
}